                    <h2 style="text-align: center; font-size: 40px;">India Tour Packages FAQs</h2>

                  <h3>  Q. Do I need to book my flight tickets separately?</h3>
                    A. No, you don’t have to book the flights separately while making a booking of India tour packages.
                     Your flight is a part of the itinerary, and it will be included in the cost of the package. However,
                      some packages featured on the website might not include airfare. The price listing will either read 
                      'airfare/flight optional' or 'airfare/flight inclusive'. With airfare/flight optional packages, 
                      you can decide if you want to take a package with or without airfare.

                  <h3> Q. How do I know if my booking is confirmed?</h3> 
                    A. For all India tour packages’ bookings made online, you will receive a confirmation message
                     via email. This email will include your booking/reservation number and travel details. You can
                      also check your booking status online on the website by filling the details of your booking
                       reference number and your email id or mobile number.
                    
                  <h3>Q. Is everything included in the cost of the tour package?</h3>  
                    A. A  tour package usually includes your breakfast, transport, hotels, flights, 
                    sightseeing, monument entry, etc. However, if you want to pursue an activity on your own 
                    of your choice, which is not included in your travel itinerary, then the cost is on you.
                     In other words, anything which is not mentioned under the ‘Package Inclusions’ in the
                      inclusions of the travel itinerary will incur an additional cost.
                    
                    
                    
                 <h3>   Q. Does a package include transport services?</h3>
                    A. Yes, almost all tour packages include transport services. You can check the services included 
                    and excluded for a package under the inclusions on your holiday itinerary. Furthermore, you can 
                    ask your travel expert to get information on the type of transport you will be provided during your 
                    travel. For instance, a group tour on a pilgrimage trip will be provided with the transport facility
                     of a bus, and it will be a part of the itinerary.
                    
                   <h3> Q. Does the tour packages include hotels with good ratings?</h3>
                    A. Most OTAs provide and recommend hotels based on their reviews and ratings put on the internet by
                     customers and their previous customer experience. However, many companies do not guarantee hotels’ 
                     quality of service and their star ratings. Hence, it is strongly recommended that you cross-check  
                     your travel aggregator’s website and go through the reviews and photographs of the hotels before
                      booking India tour packages.
